def main():
    # Input first number
    num1 = float(input("Enter the first number: "))

    # Input second number
    num2 = float(input("Enter the second number: "))

    # Calculate the sum
    total = num1 + num2

    # Display the result
    print("The sum of", num1, "and", num2, "is:", total)

if __name__ == "__main__":
    main()
